/**	@kulho
  * @brief  Definition of timer interrupt callback.
  * @param  TIM_HandleTypeDef pointer to a TIM Time Base Handle Structure definition.
  *
  * @note   This function verifies that the main loop is finished within the given sampling
  * 		period. If not, motor is set to lowest possible speed (~2.3 pulses per minute
  * 		given that the timer is setup to 84 MHz). LED on the control panel is
  * 		switched, and terminal notification is printed. For security purposes
  * 		an infinite loop protects any further operation.
  *
  * @retval None
  */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
	if (htim->Instance == TIM5) {
		if (!period_elapsed) {
			period_elapsed = true;
			period_count++;
		} else {
			TIM2->CNT = 0;
			TIM2->ARR = 2147483647;
			TIM2->CCR1 = (int) (TIM2->ARR / 2);
			HAL_GPIO_WritePin(GPIOB, GPIO_PIN_7, GPIO_PIN_SET);
			printf("\nMain loop did not finish within sampling period!\n");
			while (1) {
			}
		}
	}
}

